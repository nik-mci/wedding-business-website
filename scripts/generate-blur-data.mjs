/**
 * Generates a blurDataURL map for every image referenced in src/.
 * Output: src/lib/blurDataUrls.js
 *
 * Usage: node scripts/generate-blur-data.mjs
 */

import sharp from "sharp";
import fs from "fs";
import path from "path";

const SRC_DIR = path.join(process.cwd(), "src");
const PUBLIC_DIR = path.join(process.cwd(), "public");
const OUT_FILE = path.join(process.cwd(), "src/lib/blurDataUrls.js");

// Collect every /assets/... path referenced in source files.
// Also catches partial paths used in data arrays (e.g. img: "beach-wedding/st regis goa.jpeg")
// by checking if the quoted string resolves to a real file under public/assets/photos/.
function collectImagePaths() {
  const paths = new Set();
  // Full paths: "/assets/photos/foo.jpg"
  const reFull = /["'`](\/assets\/photos\/[^"'`\s]+\.(jpg|jpeg|png|webp|avif))["'`]/g;
  // Partial paths inside data arrays: "beach-wedding/foo.jpg"
  const rePartial = /"([^"\/][^"]*\.(jpg|jpeg|png|webp|avif))"/g;

  const walk = (dir) => {
    for (const entry of fs.readdirSync(dir, { withFileTypes: true })) {
      const full = path.join(dir, entry.name);
      if (entry.isDirectory()) { walk(full); continue; }
      if (!/\.(js|jsx|ts|tsx)$/.test(entry.name)) continue;
      const src = fs.readFileSync(full, "utf8");
      for (const [, imgPath] of src.matchAll(reFull)) paths.add(imgPath);
      for (const [, partial] of src.matchAll(rePartial)) {
        const candidate = `/assets/photos/${partial}`;
        if (fs.existsSync(path.join(PUBLIC_DIR, candidate))) paths.add(candidate);
      }
    }
  };
  walk(SRC_DIR);
  return [...paths].sort();
}

async function toBlurDataUrl(absPath) {
  try {
    const buf = await sharp(absPath)
      .resize(10, 10, { fit: "cover" })
      .jpeg({ quality: 40 })
      .toBuffer();
    return `data:image/jpeg;base64,${buf.toString("base64")}`;
  } catch {
    return null;
  }
}

async function main() {
  const paths = collectImagePaths();
  console.log(`Found ${paths.length} unique image paths in source files\n`);

  const map = {};
  let ok = 0, skip = 0;

  for (const imgPath of paths) {
    const abs = path.join(PUBLIC_DIR, imgPath);
    if (!fs.existsSync(abs)) { skip++; console.log(`SKIP  ${imgPath}`); continue; }
    const blur = await toBlurDataUrl(abs);
    if (blur) { map[imgPath] = blur; ok++; process.stdout.write("."); }
    else { skip++; }
  }

  console.log(`\n\nGenerated ${ok} blur URLs, skipped ${skip}\n`);

  // Ensure output directory exists
  fs.mkdirSync(path.dirname(OUT_FILE), { recursive: true });

  const content =
    `// Auto-generated by scripts/generate-blur-data.mjs — do not edit manually\n` +
    `const blurDataUrls = ${JSON.stringify(map, null, 2)};\n\n` +
    `export const getBlurProps = (src) => {\n` +
    `  const url = blurDataUrls[src];\n` +
    `  return url ? { placeholder: "blur", blurDataURL: url } : {};\n` +
    `};\n\n` +
    `export default blurDataUrls;\n`;

  fs.writeFileSync(OUT_FILE, content, "utf8");
  console.log(`Written to src/lib/blurDataUrls.js`);
}

main().catch(console.error);
