#     extract that content to JSON, then chunk it here.
#   - TODO(wedding): DESTINATION_KEYWORDS is a travel list — replace with wedding cities +
#     venue types so detect_destination() tags chunks correctly.
#   - SKIP_TYPES / BAD_PATTERNS: keep; just tune the wording for wedding pages.
